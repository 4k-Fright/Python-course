#Assignment operators are used to assign values to variables:


# =
x = 5
print(x)



# +=
x += 10
# same as x = x + 10
print(x)


# -=
x -= 5
# same as x = x - 5
print(x)


# *=
x *= 3
# same as x = x * 3
print(x)


# /=
x /= 3
# same as x = x / 3
print(x)

# %=
x %= 3
# same as x = x % 3
print(x)


# **=
x **= 3
# same as x = x ** 3
print(x)